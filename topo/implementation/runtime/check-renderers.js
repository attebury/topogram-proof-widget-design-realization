export function renderAppBasicRuntimeCheckCreatePayload() {
  return `function createPayload(overrides = {}) {
  return {
    title: "Runtime Check Item",
    description: "Generated by the richer runtime check bundle.",
    priority: "high",
    collection_id: envValue(plan.env.demoContainerId),
    ...(envValue(plan.env.demoMemberId) ? { owner_id: envValue(plan.env.demoMemberId) } : {}),
    ...overrides
  };
}`;
}

export function renderAppBasicRuntimeCheckHelpers() {
  return `async function waitForExportJob(jobId, { attempts = 10, delayMs = 25 } = {}) {
  for (let attempt = 0; attempt < attempts; attempt += 1) {
    const { response, responseBody } = await requestContract("cap_get_item_export_job", {
      pathParams: {
        job_id: jobId
      }
    });
    assertCondition(response.status === 200, \`get export job expected 200 during polling, got \${response.status}\`);
    if (responseBody?.status === "completed" && responseBody?.download_url) {
      return responseBody;
    }
    await sleep(delayMs);
  }
  throw new Error("export job did not reach completed status in time");
}`;
}

